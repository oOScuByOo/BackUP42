int		ft_atoi(char *str)
{
	int neg;
	int num;
	int i;

	neg = 1;
	num = 0;
	i = 0;
	while (str[i] <= ' ' && str[i] <= 1 && str[i] >= 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
		{
			neg *= -1;
		}
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + (str[i] - 48);
		i++;
	}
	return (num * neg);
}
